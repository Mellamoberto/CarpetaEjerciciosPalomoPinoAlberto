package com.example.primerospasos

fun main() {
    var valor1 : Int
    var valor2 : Int
    valor1 = 100
    valor2 = 200

    var valor3 = 7
    var valor4 = 3

    var resultado : Int
    var resultado2 : Int
    var resultadoResta : Int

    resultado = valor1 + valor2
    resultado2 = valor3 + valor4
    resultadoResta = valor1 - valor2

    println("La suma de $valor1 + $valor2 es $resultado")
    println("La suma de $valor3 + $valor4 es $resultado2")

    println("La resta de $valor1 - $valor2 es $resultadoResta")
}